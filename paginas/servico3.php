        <div class="nuvens-textos">
        <div class="nuvens-bloco">
            <h3>O que é Fotogrametria?</h3>
            <p>
            A fotogrametria é uma técnica avançada que transforma imagens bidimensionais em representações tridimensionais de alta precisão. 
            Ao capturar múltiplas fotografias de um objeto ou área a partir de diferentes ângulos, e processá-las com software especializado, 
            é possível gerar nuvens de pontos densas e detalhadas — conjuntos de milhões de pontos que representam a geometria real de superfícies, estruturas ou terrenos.
            </p>
        </div>

        <div class="nuvens-bloco">
            <h3>Como Funciona?</h3>
            <p>
            O processo começa com a recolha de imagens, geralmente feitas por UAVs/drones ou câmaras terrestres. A partir da sobreposição destas imagens, 
            o software identifica pontos em comum — chamados pontos de controlo — e calcula a sua posição no espaço tridimensional. 
            O resultado é uma nuvem de pontos que reflete com grande rigor a forma, dimensão e textura do objeto ou ambiente captado.
            </p>
        </div>

        <div class="nuvens-bloco1">
            <h3 class="vantagem">Vantagens</h3>
            <p>
            <span class="bullet"></span> <strong>Alta precisão:</strong> Ideal para medições, documentação e modelação.<br>
            <span class="bullet"></span> <strong>Versatilidade:</strong> Aplicável em terrenos, fachadas, interiores, património, etc.<br>
            <span class="bullet"></span> <strong>Custo-efetivo:</strong> Reduz a necessidade de equipamentos de levantamento convencionais.<br>
            <span class="bullet"></span> <strong>Visualização clara:</strong> Permite análises e simulações em ambiente digital 3D.
            </p>
            <p class="nuvens-final">
            Na SupremeXpansion, combinamos fotogrametria com a nossa experiência em modelação e levantamento para fornecer 
            nuvens de pontos fiáveis, prontas a ser integradas em qualquer fase do projeto.
            </p>
        </div>
